def navigate(map):
    right = 3
    down = 1
    trees = 0
    length = len(map)
    breite = len(map[down])
    while down < length:
        if right >= breite:
            right -= breite
        if map[down][right] == "#":
            string = map[down]
            string = string[:right] + "X" + string[right + 1:]
            map[down] = string
            trees += 1
        else:
            string = map[down]
            string = string[:right] + "0" + string[right + 1:]
            map[down] = string
        down += 1
        right += 3

    return trees

